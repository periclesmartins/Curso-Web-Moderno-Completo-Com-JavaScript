// Declaração das variáveis num1 e num2
let num1 = 1
let num2 = 2

// Incremento da variável num1
num1++

// Exibe o valor de num1 após o incremento
console.log(num1)

// Decremento da variável num1 com a pré-decrementação
--num1

// Exibe o valor de num1 após o decremento com a pré-decrementação
console.log(num1)

// Incremento da variável num1 com pré-incrementação e comparação do valor com o pós-decremento de num2
console.log(++num1 === num2--)

// Compara o valor atual de num1 com o valor atual de num2
console.log(num1 === num2)